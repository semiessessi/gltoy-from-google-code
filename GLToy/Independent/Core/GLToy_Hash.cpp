//
// This implements Bernstein's hash
//
GLToy_Hash GLToy_GetHash( const char* szString )
{
    GLToy_Hash uHash = 0;

    while( *szString )
    {
        uHash += uHash << 5;
        uHash += *szString;

        ++szString;
    }

    return uHash;
}