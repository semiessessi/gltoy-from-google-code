bool GLToy_Render::Initialise()
{
    if( !Platform_Initialise() )
    {
        return false;
    }

    if( !Project_Initialise() )
    {
        return false;
    }

    return true;
}

void GLToy_Render::Shutdown()
{
    Project_Shutdown();

    Platform_Shutdown();
}

void GLToy_Render::Render()
{
    Platform_BeginRender();

    Project_Render();

    Platform_EndRender();
}