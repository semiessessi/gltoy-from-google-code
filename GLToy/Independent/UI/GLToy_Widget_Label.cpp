// F U N C T I O N S
/////////////////////////////////////////////////////////////////////////////////////////////

void GLToy_Widget_Label::Render2D() const
{
    GLToy_Font* const pxFont = GLToy_Font_System::FindFont( GetFont() );
    if( !pxFont )
    {
        return;
    }

    pxFont->RenderString( m_szString, GetX(), GetY() - pxFont->GetHeight() );
}
